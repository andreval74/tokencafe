        <h1>Política de Privacidade – TokenCafe.app</h1>
        <p><strong>Última atualização:</strong> 19 de março de 2026</p>
        <p>A TokenCafe.app considera de extrema importância a proteção da privacidade e a segurança dos dados. Esta Política de Privacidade foi elaborada em conformidade com a Lei Geral de Proteção de Dados (LGPD – Lei nº 13.709/2018) e o Regulamento Geral de Proteção de Dados da União Europeia (RGPD/GDPR), adotando os princípios de finalidade, adequação, necessidade, transparência, segurança, prevenção, não discriminação, responsabilidade e ética.</p>
        <div class="important">
          <strong>IMPORTANTE:</strong> O TokenCafe.app é uma plataforma Web3 descentralizada.
          Não realizamos KYC, não mantemos banco de dados central de usuários, não coletamos CPF, RG, nome, endereço, telefone ou qualquer dado sensível.
          O acesso ocorre exclusivamente via conexão de carteira digital (endereço público on-chain).
          Não armazenamos contratos gerados (eles ficam na blockchain pública). Coletamos o mínimo possível de informações técnicas.
        </div>
        <h2>1. Dados que Coletamos</h2>
        <p>Coletamos apenas o estritamente necessário para o funcionamento técnico:</p>
        <ul>
          <li>Endereço público da carteira (ex.: 0x123…abc) – fornecido voluntariamente ao conectar a wallet;</li>
          <li>Endereço IP e dados técnicos básicos (navegador, sistema operacional, cookies de sessão);</li>
          <li>Dados de navegação agregados para melhoria da experiência.</li>
        </ul>
        <p><strong>NÃO coletamos:</strong> nome, CPF, RG, e-mail, telefone, dados bancários, geolocalização precisa ou qualquer dado sensível. Não armazenamos contratos nem histórico de usuários.</p>
        <h2>2. Bases Legais e Finalidades do Tratamento</h2>
        <p>Tratamos os dados com base em:</p>
        <ul>
          <li>Execução de contrato técnico (conexão da carteira e prestação dos Serviços);</li>
          <li>Interesse legítimo (segurança, prevenção de abusos e melhoria da plataforma);</li>
          <li>Obrigações legais (quando exigido por autoridade competente).</li>
        </ul>
        <p>Finalidades: permitir o funcionamento da plataforma, garantir segurança, prevenir fraudes e aprimorar a experiência técnica.</p>
        <h2>3. Compartilhamento de Dados</h2>
        <p>Não vendemos dados. Compartilhamos apenas com:</p>
        <ul>
          <li>Provedores técnicos essenciais (hospedagem, serviços blockchain, monitoramento de performance) sob contratos de proteção de dados;</li>
          <li>Autoridades judiciais ou regulatórias, exclusivamente quando obrigados por lei.</li>
        </ul>
        <h2>4. Transferência Internacional de Dados</h2>
        <p>Como o serviço é global e descentralizado, dados técnicos mínimos podem ser processados em servidores no exterior. Todas as transferências seguem Cláusulas Contratuais Padrão (ou equivalentes) e garantem nível adequado de proteção conforme LGPD e RGPD. Fornecemos transparência total aos titulares.</p>
        <h2>5. Cookies e Tecnologias Semelhantes</h2>
        <p>Usamos apenas cookies técnicos essenciais (necessários para sessão e segurança). Não utilizamos cookies de publicidade ou rastreamento comportamental. Você pode gerenciá-los ou bloqueá-los no navegador a qualquer momento.</p>
        <h2>6. Segurança da Informação</h2>
        <p>Adotamos medidas técnicas, organizacionais e de governança (criptografia, controle de acesso restrito, arquitetura descentralizada) para proteger os dados. Embora nenhum sistema seja 100% seguro, seguimos as melhores práticas da indústria. A segurança final da sua carteira e chaves privadas é sua responsabilidade exclusiva.</p>
        <h2>7. Seus Direitos (LGPD/RGPD)</h2>
        <p>Você tem direito a:</p>
        <ul>
          <li>Confirmação da existência de tratamento;</li>
          <li>Acesso, correção, portabilidade e eliminação dos dados;</li>
          <li>Revogação de consentimento (quando aplicável);</li>
          <li>Oposição ao tratamento;</li>
          <li>Informação sobre compartilhamento.</li>
        </ul>
        <p>Para exercer qualquer direito, envie e-mail para <a href="mailto:contact@tokencafe.app">contact@tokencafe.app</a>. Responderemos em até 15 dias úteis.</p>
        <h2>8. Prazo de Retenção</h2>
        <p>Os dados técnicos são mantidos apenas pelo tempo necessário (máximo 90 dias para logs de segurança). Não mantemos histórico de usuários nem contratos.</p>
