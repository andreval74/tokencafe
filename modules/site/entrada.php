<script>
  (function() {
    try {
      document.documentElement.style.scrollBehavior = "smooth";
    } catch (e) {}
  })();

  // Abre o widget de IA via window.tcIaOpen (exposto por ia-widget.js após DOMContentLoaded)
  // Aguarda DOMContentLoaded para garantir que ia-widget.js já inicializou e registrou window.tcIaOpen
  (function() {
    function openIa() {
      if (typeof window.tcIaOpen === 'function') {
        window.tcIaOpen();
      } else {
        document.getElementById('tc-ia-header-btn')?.click();
      }
    }

    function bindIaButtons() {
      var el = document.getElementById('btnOpenIaFromEntry');
      if (el) el.addEventListener('click', openIa);
    }
    if (document.readyState === 'loading') {
      document.addEventListener('DOMContentLoaded', bindIaButtons);
    } else {
      bindIaButtons();
    }
  })();

  (function() {
    function handleConnectClick(el) {
      el.addEventListener("click", function(e) {
        e.preventDefault();
        var dest = el.getAttribute("href") || "index.php?page=tools";
        try {
          sessionStorage.setItem("tokencafe_post_connect_redirect", JSON.stringify({
            href: dest,
            ts: Date.now()
          }));
        } catch (_) {}
        var status = (window.walletConnector && window.walletConnector.getStatus) ? window.walletConnector.getStatus() : {};
        if (status.isConnected && status.sessionAuthorized) {
          window.location.href = dest;
          return;
        }
        if (window.authModal && typeof window.authModal.show === "function") {
          window.authModal.show();
        } else {
          document.dispatchEvent(new CustomEvent("tc:open-auth-modal"));
        }
      });
    }

    function bindConnectLinks() {
      document.querySelectorAll("[data-tc-connect]").forEach(handleConnectClick);
    }

    if (document.readyState === "loading") {
      document.addEventListener("DOMContentLoaded", bindConnectLinks);
    } else {
      bindConnectLinks();
    }
  })();
</script>
